package week1.day1;

public class Learndatatypes {

public static void main(String[] args) {
	
	//Primitive Datatypes
	byte age=30;	
	System.out.println(age);
	
	short ageOfPerson=128;
	short amount=30000;
	int depositAmount=50000;
	long mobileNumber=9123456789l;
	float rateOfInt=7.5f;
	float rupee=1.123456789F;
	double usd=1.123456789;		
	System.out.println(rupee);
	System.out.println(usd);
	
	boolean haveBreakfast=false;
	System.out.println(haveBreakfast);
	
	char logo='T';
	System.out.println(logo);
	
	//Non Primitive Datatypes - Predefined class
	String name="Belcy";
	System.out.println(name);

	}

}
